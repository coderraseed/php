 /* 
 !How many types of database?
 There are several types of databases, each with its own strengths and weaknesses. Some of the most common types of databases are:

    Relational databases: These are the most commonly used databases and store data in tables with predefined relationships between them.

    NoSQL databases: These databases are designed for handling unstructured data, such as JSON or XML, and do not use a traditional table structure.

    Object-oriented databases: These databases store data in objects rather than tables, making them more suitable for object-oriented programming languages.

    Graph databases: These databases store data in nodes and edges, making them ideal for representing complex relationships between entities.

    In-memory databases: These databases store data in memory rather than on disk, providing faster access to data but requiring more memory.

    Distributed databases: These databases store data across multiple servers, making them ideal for large-scale applications.

    Time-series databases: These databases are optimized for handling time-series data, such as stock market data or sensor data.

    Document-oriented databases: These databases store data in documents, making them suitable for storing unstructured data like text documents or web pages.
  */

  /* 
  !Relational databases
  Relational databases are the most common type of database used in modern applications. They store data in tables that are related to each other through common fields. The data in a relational database is organized in rows and columns, with each column representing a specific piece of data, and each row representing a record or a set of related data.

    The relational database model is based on the concept of normalization, which is the process of organizing data into tables and minimizing redundancy. This allows for efficient data retrieval, as data is stored in a structured and organized way.

    Relational databases use a structured query language (SQL) to manipulate and retrieve data. SQL is a standard language used across many database systems, and it allows users to perform complex queries and retrieve specific data from the database.

    One of the main benefits of relational databases is their ability to enforce data integrity through the use of constraints and relationships between tables. This helps to ensure that data is accurate and consistent across the database. Relational databases are used in a wide range of applications, including enterprise resource planning systems, customer relationship management systems, and financial applications.


   */

   /* 
   !NoSQL databases
   NoSQL databases, also known as non-relational databases, are databases that do not use a traditional table structure with fixed columns and rows. Instead, they are designed to handle unstructured or semi-structured data, such as documents, graphs, key-value pairs, or JSON data.

NoSQL databases are typically more scalable and flexible than traditional relational databases, and they can handle large amounts of unstructured data with ease. They are often used in applications that require high performance, low latency, and real-time data processing.

There are several types of NoSQL databases, including:

    Document databases: These databases store data in documents, such as JSON or XML files. They are designed for handling semi-structured data and can scale horizontally.

    Key-value databases: These databases store data as key-value pairs, allowing for fast access to data. They are often used for caching and session management.

    Graph databases: These databases store data as nodes and edges, allowing for complex relationships between entities. They are often used for social networks, recommendation systems, and fraud detection.

    Column-family databases: These databases store data in column families, which are groups of columns that are stored together. They are designed for handling large amounts of data and can scale horizontally.

NoSQL databases are often used in web applications, mobile applications, and big data processing. They are also used in real-time analytics, content management systems, and Internet of Things (IoT) applications.
    */